import streamlit as st
import joblib
import pandas as pd 

model = joblib.load("models/heart_disease_model.pkl")
scaler = joblib.load("models/scaler.pkl")

# ---------------- Sidebar ----------------
st.sidebar.title("❤️ Heart Disease Prediction")
st.sidebar.markdown("---")
st.sidebar.write("**Model:** Logistic Regression")
st.sidebar.write("**Accuracy:** 89.13%")
st.sidebar.write("**Developer:** Ansh Gupta")
st.sidebar.markdown("---")


st.title("Heart Disease Prediction")

st.image("images/banner.png", width=380)

st.markdown(
"Predict the likelihood of heart disease using a machine learning model trained on clinical patient data."
)
with st.expander("⚠️ Medical Disclaimer"):
    st.write(
        "This prediction is generated by a machine learning model for educational purposes only. "
        "It is not a substitute for professional medical advice, diagnosis, or treatment. "
        "Always consult a qualified healthcare professional regarding any medical concerns."
    )
st.write("Enter Patients Details Below .") 

age=st.number_input("Age:",18,120)
resting_bp=st.number_input("Resting blood presure:")
cholesterol=st.number_input("Cholesterol:")
fasting_bs=st.selectbox("Fasting blood sugar > 120 mg/dL",
                        ["Yes","No"])
MaxHR=st.number_input("Maximum Heart Rate:")
oldpeak=st.number_input("Oldpeak",step=0.1)

sex=st.selectbox("SEX",["Male","Female"])
chestpain_type=st.selectbox("Chest Pain Type",["ASY","ATA","NAP","TA"])
resting_ecg=st.selectbox("Resting ECG",["Normal","ST","LVH"])
exercise_angina=st.selectbox("Exercise Induced Angin",["Yes","No"])
st_slope=st.selectbox("ST_Slope",["Down","Up","Flat"])

if st.button("❤️ Predict Heart Disease", use_container_width=True):
    st.subheader("Patient Summary")
    
    st.write(f"Age: {age}")
    st.write(f"Resting Blood Pressure: {resting_bp}")
    st.write(f"Cholesterol: {cholesterol}")
    st.write(f"fasting Blood Sugar: {fasting_bs}")
    st.write(f"Maximum Heart Rate: {MaxHR}")
    st.write(f"Old Peak: {oldpeak}")
    st.write(f"Sex: {sex}")
    st.write(f"Chest Pain Type: {chestpain_type}")
    st.write(f"Resting ECG: {resting_ecg}")
    st.write(f"Exercise Angina: {exercise_angina}")
    st.write(f"ST_Slope: {st_slope}")

# Converting categorical features to numeric 

    Sex_M=1 if sex=="Male" else 0

    FastingBS=1 if fasting_bs=="Yes" else 0

    ChestPainType_ATA = 1 if chestpain_type == "ATA" else 0
    ChestPainType_NAP = 1 if chestpain_type == "NAP" else 0
    ChestPainType_TA = 1 if chestpain_type == "TA" else 0

    RestingECG_Normal = 1 if resting_ecg == "Normal" else 0
    RestingECG_ST = 1 if resting_ecg == "ST" else 0

    ExerciseAngina_Y = 1 if exercise_angina == "Yes" else 0

    ST_Slope_Flat = 1 if st_slope == "Flat" else 0
    ST_Slope_Up = 1 if st_slope == "Up" else 0


    features = pd.DataFrame({
        'Age':[age],
        'RestingBP':[resting_bp],
        'Cholesterol':[cholesterol],
        'FastingBS':[FastingBS],
        'MaxHR':[MaxHR],
        'Oldpeak':[oldpeak],
        'Sex_M':[Sex_M],
        'ChestPainType_ATA':[ChestPainType_ATA],
        'ChestPainType_NAP':[ChestPainType_NAP],
        'ChestPainType_TA':[ChestPainType_TA],
        'RestingECG_Normal':[RestingECG_Normal],
        'RestingECG_ST':[RestingECG_ST],
        'ExerciseAngina_Y':[ExerciseAngina_Y],
        'ST_Slope_Flat':[ST_Slope_Flat],
        'ST_Slope_Up':[ST_Slope_Up]
    })

    # Scaling 

    numerical_cols = [
        'Age',
        'RestingBP',
        'Cholesterol',
        'MaxHR',
        'Oldpeak'
    ]

    features[numerical_cols]=scaler.transform(features[numerical_cols])

    prediction = model.predict(features)[0]
    probabilities = model.predict_proba(features)[0]
    

    if prediction == 1:
        st.error("⚠️ High Risk Of Heart Disease")
    else:
        st.success("✅ Low Risk Of Heart Disease")

    st.write(f"Probability of Heart Disease: {probabilities[1]:.2%}")
    st.write(f"Probability of No Heart Disease: {probabilities[0]:.2%}")
    

st.markdown("---")
st.caption("Developed by Ansh Gupta | Built with Python, Streamlit & Scikit-learn")
