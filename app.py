st.markdown("<div class='section'>📝 Enter Patient Details</div>",unsafe_allow_html=True)

age=st.number_input("👤 Age",18,120)
resting_bp=st.number_input("🩸 Resting Blood Pressure (mmHg)")
cholesterol=st.number_input("🧪 Cholesterol (mg/dL)")
fasting_bs=st.selectbox("🍬 Fasting Blood Sugar > 120 mg/dL",["Yes","No"])
MaxHR=st.number_input("❤️ Maximum Heart Rate (bpm)")
oldpeak=st.number_input("📈 Oldpeak (ST Depression)",step=0.1)
sex=st.selectbox("⚧ Sex",["Male","Female"])
chestpain_type=st.selectbox("🫀 Chest Pain Type",["ASY","ATA","NAP","TA"])
resting_ecg=st.selectbox("📊 Resting ECG",["Normal","ST","LVH"])
exercise_angina=st.selectbox("⚡ Exercise Induced Angina",["Yes","No"])
st_slope=st.selectbox("📉 ST Slope",["Down","Up","Flat"])

if st.button("❤️ Predict Heart Disease"):
    with st.spinner("Analyzing patient data..."):
        time.sleep(0.7)

    Sex_M=1 if sex=="Male" else 0
    FastingBS=1 if fasting_bs=="Yes" else 0
    ChestPainType_ATA=1 if chestpain_type=="ATA" else 0
    ChestPainType_NAP=1 if chestpain_type=="NAP" else 0
    ChestPainType_TA=1 if chestpain_type=="TA" else 0
    RestingECG_Normal=1 if resting_ecg=="Normal" else 0
    RestingECG_ST=1 if resting_ecg=="ST" else 0
    ExerciseAngina_Y=1 if exercise_angina=="Yes" else 0
    ST_Slope_Flat=1 if st_slope=="Flat" else 0
    ST_Slope_Up=1 if st_slope=="Up" else 0

    features=pd.DataFrame({
        'Age':[age],'RestingBP':[resting_bp],'Cholesterol':[cholesterol],
        'FastingBS':[FastingBS],'MaxHR':[MaxHR],'Oldpeak':[oldpeak],
        'Sex_M':[Sex_M],'ChestPainType_ATA':[ChestPainType_ATA],
        'ChestPainType_NAP':[ChestPainType_NAP],'ChestPainType_TA':[ChestPainType_TA],
        'RestingECG_Normal':[RestingECG_Normal],'RestingECG_ST':[RestingECG_ST],
        'ExerciseAngina_Y':[ExerciseAngina_Y],'ST_Slope_Flat':[ST_Slope_Flat],
        'ST_Slope_Up':[ST_Slope_Up]
    })
    numerical_cols=['Age','RestingBP','Cholesterol','MaxHR','Oldpeak']
    features[numerical_cols]=scaler.transform(features[numerical_cols])
    prediction=model.predict(features)[0]
    probabilities=model.predict_proba(features)[0]

    st.markdown("---")
    st.subheader("📋 Patient Summary")
    st.write(f"**Age:** {age}")
    st.write(f"**Blood Pressure:** {resting_bp}")
    st.write(f"**Cholesterol:** {cholesterol}")
    st.write(f"**Maximum Heart Rate:** {MaxHR}")

    st.markdown("### 📊 Prediction")
    st.progress(float(probabilities[1]))

    if prediction==1:
        st.error(f"⚠️ High Risk of Heart Disease\n\nProbability: **{probabilities[1]:.2%}**")
        st.warning("Recommendation: Please consult a qualified healthcare professional.")
    else:
        st.success(f"✅ Low Risk of Heart Disease\n\nProbability: **{probabilities[0]:.2%}**")
        st.info("Recommendation: Maintain a healthy lifestyle and regular health checkups.")

